    private void jcomdrinksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcomdrinksActionPerformed
        double ThumbsUp=1.20;
        double Sprite=1.21;
        double Mazza=1.22;
        double Pepsi=1.23;
        if(jcomdrinks.getSelectedItem().equals("ThumbsUp"))
        {
            String cThumbsUp=String.format(" $ %.2f", ThumbsUp);
            jcostofdrinks.setText(cThumbsUp);
        }
        if(jcomdrinks.getSelectedItem().equals("Sprite"))
        {
            String cSprite=String.format(" $ %.2f",Sprite);
            jcostofdrinks.setText(cSprite);
        }
        if(jcomdrinks.getSelectedItem().equals("Mazza"))
        {
            String cMazza=String.format(" $ %.2f", Mazza);
            jcostofdrinks.setText(cMazza);
        }
        if(jcomdrinks.getSelectedItem().equals("Pepsi"))
        {
            String cPepsi=String.format(" $ %.2f", Pepsi);
            jcostofdrinks.setText(cPepsi);
        }
        if(jcomdrinks.getSelectedItem().equals("Select a drink"))
        {
                        jcostofdrinks.setText(null);
        }
    }//GEN-LAST:event_jcomdrinksActionPerformed

